
/// Основные временные промежутки
abstract class AppDuration {
  static const oneSecond = Duration(seconds: 1);
  static const twoSecond = Duration(seconds: 2);
  static const tenSecond = Duration(seconds: 10);
  static const animation = Duration(milliseconds: 300);
